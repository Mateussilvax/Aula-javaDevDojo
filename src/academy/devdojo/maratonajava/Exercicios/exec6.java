public class exec6 {
    //6. Contagem Regressiva
    //
    //Escreva um algoritmo que exiba todos os números de 20 até 1 em ordem decrescente, utilizando um laço de repetição.
    public static void main(String[] args) {

        for (int i = 20; i > 0 ; i--) {
            System.out.println(i);
        }
    }
}
